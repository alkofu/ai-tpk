// ---------------------------------------------------------------------------
// All agents: structural correctness loop
// ---------------------------------------------------------------------------
Deno.test(
  "generateClaudeAgent - all agents have correct structure",
  async () => {
    for await (const entry of Deno.readDir(SRC_DIR)) {
      if (!entry.name.endsWith(".md")) continue;
      const srcPath = join(SRC_DIR, entry.name);
      const agentName = entry.name.replace(/\.md$/, "");
      const generated = await generateClaudeAgent(srcPath);

      // Header present
      assert(
        generated.includes(
          "<!-- Generated by ai-tpk adapters. Do not edit directly. -->",
        ),
        `${agentName}: missing generated header`,
      );

      // name: field matches filename-derived name
      assert(
        /^name: /m.test(generated),
        `${agentName}: frontmatter must contain 'name:' field`,
      );
      assert(
        generated.includes(`name: ${agentName}`),
        `${agentName}: name field must match filename-derived name`,
      );

      // description: field present and non-empty
      assert(
        /^description: ".+"/m.test(generated),
        `${agentName}: must have non-empty description`,
      );

      // Non-empty body after closing ---
      const lines = generated.split("\n");
      let delimCount = 0;
      let bodyStart = -1;
      for (let i = 0; i < lines.length; i++) {
        if (lines[i] === "---") {
          delimCount++;
          if (delimCount === 2) {
            bodyStart = i + 1;
            break;
          }
        }
      }
      assert(
        bodyStart !== -1,
        `${agentName}: must have closing --- delimiter`,
      );
      const body = lines.slice(bodyStart).join("\n");
      assert(
        body.trim().length > 0,
        `${agentName}: body must be non-empty`,
      );
    }
  },
);

