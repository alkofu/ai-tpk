// adapters/to-claude_test.ts — Tests for the Claude Code adapter.
//
// Verifies structural correctness of generateClaudeAgent() output.
// No reliance on committed baseline files — tests run on a clean checkout.

import { assert, assertEquals, assertMatch, assertNotMatch } from "@std/assert";
import { exists } from "@std/fs";
import { join } from "@std/path";
import { generateClaudeAgent } from "./to-claude.ts";

const REPO_ROOT = join(import.meta.dirname!, "..");
const SRC_DIR = join(REPO_ROOT, "src", "agents");

// ---------------------------------------------------------------------------
// bitsmith: structural checks — has header, name, description, non-empty body
// ---------------------------------------------------------------------------
Deno.test("generateClaudeAgent bitsmith has generated header", async () => {
  const srcFile = join(SRC_DIR, "bitsmith.md");
  const generated = await generateClaudeAgent(srcFile);

  assertMatch(
    generated,
    /<!-- Generated by ai-tpk adapters\. Do not edit directly\. -->/,
    "must contain generated header comment",
  );
});

Deno.test(
  "generateClaudeAgent bitsmith has name: bitsmith in frontmatter",
  async () => {
    const srcFile = join(SRC_DIR, "bitsmith.md");
    const generated = await generateClaudeAgent(srcFile);

    assertMatch(
      generated,
      /^name: bitsmith$/m,
      "must contain 'name: bitsmith'",
    );
  },
);

Deno.test(
  "generateClaudeAgent bitsmith has non-empty description",
  async () => {
    const srcFile = join(SRC_DIR, "bitsmith.md");
    const generated = await generateClaudeAgent(srcFile);

    assertMatch(
      generated,
      /^description: ".+"/m,
      "must contain non-empty description",
    );
  },
);

Deno.test("generateClaudeAgent bitsmith has non-empty body", async () => {
  const srcFile = join(SRC_DIR, "bitsmith.md");
  const generated = await generateClaudeAgent(srcFile);

  // Extract body after closing ---
  const lines = generated.split("\n");
  let delimCount = 0;
  let bodyStart = -1;
  for (let i = 0; i < lines.length; i++) {
    if (lines[i] === "---") {
      delimCount++;
      if (delimCount === 2) {
        bodyStart = i + 1;
        break;
      }
    }
  }
  assert(bodyStart !== -1, "must have closing --- delimiter");
  const body = lines.slice(bodyStart).join("\n");
  assert(body.trim().length > 0, "body after frontmatter must be non-empty");
});

Deno.test(
  "generateClaudeAgent bitsmith does not have invoke_when",
  async () => {
    const srcFile = join(SRC_DIR, "bitsmith.md");
    const generated = await generateClaudeAgent(srcFile);

    assertNotMatch(
      generated,
      /^invoke_when:/m,
      "bitsmith must not contain 'invoke_when:'",
    );
  },
);

